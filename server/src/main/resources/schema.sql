DROP MATERIALIZED VIEW IF EXISTS learn_language.card_view;

CREATE MATERIALIZED VIEW learn_language.card_view AS
SELECT
    c.id,
    c.source_id,
    c.source_page_number,
    c.data,
    c.readiness,
    c.due,
    c.reps,
    c.state,
    c.last_review,
    c.flagged,
    s.card_type,
    rs.review_score,
    cs.correct_streak,
    CASE WHEN c.readiness IN ('READY', 'REVIEWED', 'KNOWN') AND (
        (c.data->'translation'->>'en' IS NULL OR TRIM(c.data->'translation'->>'en') = '') OR
        (c.data->'translation'->>'hu' IS NULL OR TRIM(c.data->'translation'->>'hu') = '') OR
        (c.data->'translation'->>'ch' IS NULL OR TRIM(c.data->'translation'->>'ch') = '') OR
        (s.card_type = 'VOCABULARY' AND c.data->>'type' = 'NOUN' AND (c.data->>'gender' IS NULL OR TRIM(c.data->>'gender') = '')) OR
        (s.card_type = 'VOCABULARY' AND (c.data->>'type' IS NULL OR TRIM(c.data->>'type') = ''))
    ) THEN true ELSE false END AS is_unhealthy,
    CASE WHEN c.readiness = 'READY'
        AND c.state = 'REVIEW'
        AND c.stability >= 30
        AND c.reps >= 5
        AND c.lapses <= 1
    THEN true ELSE false END AS is_suggested_known
FROM learn_language.cards c
JOIN learn_language.sources s ON c.source_id = s.id
LEFT JOIN LATERAL (
    SELECT COALESCE(MIN(rn) FILTER (WHERE rating < 3) - 1,
                    MAX(rn))::integer AS correct_streak
    FROM (
        SELECT rating, ROW_NUMBER() OVER (ORDER BY review DESC) AS rn
        FROM learn_language.review_logs
        WHERE card_id = c.id
    ) ranked
) cs ON true
LEFT JOIN LATERAL (
    SELECT
        CASE
            WHEN stats.total_count = 0 THEN NULL
            WHEN stats.total_count = 1 THEN (stats.last_review_success * 100)::integer
            ELSE ROUND(
                (0.5 * stats.last_review_success + 0.5 * stats.prev_success_rate) * 100
            )::integer
        END AS review_score
    FROM (
        SELECT
            COUNT(*) AS total_count,
            CASE WHEN (SELECT rating FROM learn_language.review_logs WHERE card_id = c.id ORDER BY review DESC LIMIT 1) >= 3
                THEN 1.0 ELSE 0.0
            END AS last_review_success,
            COALESCE(
                (SELECT AVG(CASE WHEN rating >= 3 THEN 1.0 ELSE 0.0 END)
                 FROM (SELECT rating FROM learn_language.review_logs WHERE card_id = c.id ORDER BY review DESC OFFSET 1) prev),
                0.0
            ) AS prev_success_rate
        FROM learn_language.review_logs
        WHERE card_id = c.id
    ) stats
) rs ON true;

CREATE UNIQUE INDEX IF NOT EXISTS card_view_id_idx ON learn_language.card_view (id);
